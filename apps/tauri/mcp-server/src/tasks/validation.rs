    // ── validate_task_ids_active (#2888 archived rejection) ─────────

    /// Three fixed UUIDv7-shaped strings the ID tests reuse. Pinned
    /// rather than generated so the "first missing in caller order"
    /// assertion can reference a specific literal.
    const TEST_UUID_1: &str = "01966a3f-7c8b-7d4e-8f3a-000000000001";
    const TEST_UUID_2: &str = "01966a3f-7c8b-7d4e-8f3a-000000000002";
    const TEST_UUID_3: &str = "01966a3f-7c8b-7d4e-8f3a-000000000003";

    fn insert_task_helper(conn: &Connection, id: &str, archived_at: Option<&str>) {
        // lift to canonical TaskBuilder.
        lorvex_store::test_support::fixtures::TaskBuilder::new(id)
            .title("T")
            .archived_at(archived_at)
            .insert(conn);
    }

    #[test]
    fn validate_task_ids_active_accepts_live_task() {
        let conn = setup_test_conn();
        insert_task_helper(&conn, TEST_UUID_1, None);
        let ids = vec![TEST_UUID_1.to_string()];
        assert!(validate_task_ids_active(&conn, &ids, "task_ids").is_ok());
    }

    #[test]
    fn validate_task_ids_active_rejects_nonexistent_id() {
        let conn = setup_test_conn();
        let ids = vec![TEST_UUID_1.to_string()];
        let err = validate_task_ids_active(&conn, &ids, "task_ids").unwrap_err();
        let msg = err.to_string();
        assert!(msg.contains("non-existent task"), "unexpected error: {msg}");
        assert!(msg.contains(TEST_UUID_1), "missing id in message: {msg}");
    }

    #[test]
    fn validate_task_ids_active_rejects_archived_task() {
        let conn = setup_test_conn();
        insert_task_helper(&conn, TEST_UUID_1, Some("2026-04-26T00:00:00.000Z"));
        let ids = vec![TEST_UUID_1.to_string()];
        let err = validate_task_ids_active(&conn, &ids, "task_ids").unwrap_err();
        let msg = err.to_string();
        assert!(msg.contains("archived"), "unexpected error: {msg}");
        assert!(msg.contains(TEST_UUID_1), "missing id in message: {msg}");
    }

    #[test]
    fn validate_task_ids_active_mixed_batch_reports_first_offender_in_caller_order() {
        let conn = setup_test_conn();
        // TEST_UUID_1 is live; TEST_UUID_2 is archived; TEST_UUID_3 is missing.
        insert_task_helper(&conn, TEST_UUID_1, None);
        insert_task_helper(&conn, TEST_UUID_2, Some("2026-04-26T00:00:00.000Z"));
        let ids = vec![
            TEST_UUID_1.to_string(),
            TEST_UUID_2.to_string(),
            TEST_UUID_3.to_string(),
        ];
        let err = validate_task_ids_active(&conn, &ids, "task_ids").unwrap_err();
        let msg = err.to_string();
        // Caller order: live → archived → missing. First offender is the
        // archived one.
        assert!(msg.contains("archived"), "expected archived error: {msg}");
        assert!(msg.contains(TEST_UUID_2), "expected #2 id: {msg}");
        assert!(
            !msg.contains(TEST_UUID_3),
            "should not report later phantom: {msg}"
        );
    }

    #[test]
    fn validate_task_ids_active_accepts_empty_list() {
        let conn = setup_test_conn();
        assert!(validate_task_ids_active(&conn, &[], "task_ids").is_ok());
    }

    #[test]
    fn validate_task_ids_active_rejects_malformed_uuid() {
        let conn = setup_test_conn();
        let ids = vec!["not-a-uuid".to_string()];
        let err = validate_task_ids_active(&conn, &ids, "task_ids").unwrap_err();
        assert!(err.to_string().contains("not a valid UUID"));
    }
