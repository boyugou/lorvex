/// Scrub an optional user-authored text field and re-run its length
/// validator after normalization. Callers that already use derive/manual
/// contract validation keep that first-pass guard; this closes the
/// post-sanitize boundary where NFC or control stripping can change the final
/// persisted string.
pub(crate) fn sanitize_optional_user_text(
    value: Option<String>,
    field_name: &'static str,
    max_len: usize,
) -> Result<Option<String>, McpError> {
    let sanitized = value.map(|text| lorvex_domain::sanitize_user_text(&text));
    validate_optional_string_length(sanitized.as_deref(), field_name, max_len)?;
    Ok(sanitized)
}

/// Verify that a list_id references an existing list, if provided.
pub(crate) fn validate_list_exists(
    conn: &Connection,
    list_id: Option<&str>,
) -> Result<(), McpError> {
    let Some(lid) = list_id else {
        return Ok(());
    };
    let typed = lorvex_domain::ListId::from_trusted(lid.to_string());
    Ok(lorvex_store::validate_task_list_exists(conn, &typed)?)
}

