use crate::error::McpError;
use rusqlite::Connection;
use std::collections::HashSet;

// ── Shared helpers for ID-existence validators ──────────────────────
//
// `validate_task_ids_active` and `validate_list_ids_exist` both dedup
// the caller's input (preserving insertion order so the first missing
// id is reported in caller order) and build a comma-joined `?`
// placeholder string for an `IN (...)` SQL clause. The shared helpers
// below own that boilerplate so each per-entity validator stays a
// thin call site.

/// Dedup `ids` while preserving insertion order. Returns &str views into
/// the original slice so callers can route the result straight into
/// `rusqlite::params_from_iter` without copying.
fn dedup_preserve_order(ids: &[String]) -> Vec<&str> {
    let mut seen = HashSet::with_capacity(ids.len());
    ids.iter()
        .map(String::as_str)
        .filter(|id| seen.insert(*id))
        .collect()
}

