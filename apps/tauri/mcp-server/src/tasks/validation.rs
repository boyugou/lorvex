/// Verify an ID looks like a hyphenated UUID so an obviously-bogus
/// caller argument ("task-1234", a title, empty string, etc.) fails
/// fast with an actionable "invalid UUID format" message instead of
/// a terse "non-existent task" after a DB round-trip. Accepts any
/// UUID version — we do not gate on v7 because historical IDs
/// generated by prior versions of the app may exist in synced data
/// and should remain lookupable.
///
/// Thin wrapper over the canonical
/// [`lorvex_domain::entity_id::parse_id_with_sentinel`]. The MCP
/// trust boundary has no sentinel cases (the inbox sentinel is
/// list-only and the MCP `list_id` arguments flow through
/// `resolve_required_task_list_id`, not this helper), so the
/// sentinel argument is always `None`. Trimming and the
/// empty-after-trim case both run here so whitespace-padded garbage
/// fails fast with a clearer message before hitting SQL.
pub(crate) fn validate_uuid_arg(id: &str, field_name: &'static str) -> Result<String, McpError> {
    use lorvex_domain::validation::ValidationError;
    match lorvex_domain::entity_id::parse_id_with_sentinel(id, field_name, None) {
        Ok(parsed) => Ok(parsed),
        Err(ValidationError::Empty(_)) => Err(McpError::Validation(format!(
            "{field_name} must not be empty"
        ))),
        Err(ValidationError::InvalidFormat { actual, .. }) => Err(McpError::Validation(format!(
            "{field_name} is not a valid UUID: '{actual}'"
        ))),
        Err(other) => Err(McpError::Validation(other.to_string())),
    }
}

pub(crate) fn validate_uuid_shape(id: &str, field_name: &'static str) -> Result<(), McpError> {
    validate_uuid_arg(id, field_name).map(|_| ())
}

/// Configuration for [`validate_ids_referencing`]. Each field below
/// describes one axis the three concrete validators differ on.
struct IdsValidatorOpts {
    /// Target table to query (`tasks`, `lists`, …).
    table: &'static str,
    /// Singular human-readable entity label embedded in error messages
    /// ("task", "list", …).
    entity_label: &'static str,
    /// When `true`, every id is run through `validate_uuid_shape` before
    /// any DB query — surfaces a hallucinated `"task-1234"`-shaped id
    /// with a clearer error than a post-DB "not found" miss.
    check_uuid_shape: bool,
    /// When `true`, the SELECT also reads `archived_at` and rejects
    /// archived (soft-deleted) rows with a distinct error message
    /// pointing the assistant at the Trash. The forward-planning
    /// surfaces (current focus, daily plan) need this; the dependency
    /// surface, which CAN reference archived rows, does not.
    reject_archived: bool,
}

/// Shared engine for ID-referenced existence (and optional archive)
/// validators. Encapsulates the dedup + single-IN-query + first-missing
/// detection that `validate_task_ids_active` and
/// `validate_list_ids_exist` share; the per-entity validators differ
/// only on table name and whether `archived_at` is selected.
fn validate_ids_referencing(
    conn: &Connection,
    ids: &[String],
    field_name: &'static str,
    opts: IdsValidatorOpts,
) -> Result<(), McpError> {
    for id in ids {
        if id.is_empty() {
            return Err(McpError::Validation(format!(
                "{field_name} contains an empty {} ID",
                opts.entity_label
            )));
        }
        if opts.check_uuid_shape {
            validate_uuid_shape(id, field_name)?;
        }
    }
    if ids.is_empty() {
        return Ok(());
    }

    let deduped = dedup_preserve_order(ids);
    let placeholders = lorvex_domain::sql_csv_placeholders(deduped.len());
    let sql = if opts.reject_archived {
        format!(
            "SELECT id, archived_at FROM {table} WHERE id IN ({placeholders})",
            table = opts.table,
        )
    } else {
        format!(
            "SELECT id FROM {table} WHERE id IN ({placeholders})",
            table = opts.table,
        )
    };
    let params = rusqlite::params_from_iter(deduped.iter().copied());
    let mut stmt = conn.prepare(&sql)?;

    if opts.reject_archived {
        let mut existing: std::collections::HashMap<String, Option<String>> =
            std::collections::HashMap::with_capacity(deduped.len());
        let rows = stmt.query_map(params, |row| {
            Ok((row.get::<_, String>(0)?, row.get::<_, Option<String>>(1)?))
        })?;
        for row in rows {
            let (id, archived_at) = row?;
            existing.insert(id, archived_at);
        }
        for id in ids {
            match existing.get(id.as_str()) {
                None => {
                    return Err(McpError::Validation(format!(
                        "{field_name} references non-existent {entity} '{id}'",
                        entity = opts.entity_label,
                    )));
                }
                Some(Some(_)) => {
                    return Err(McpError::Validation(format!(
                        "{field_name} references archived (trashed) {entity} '{id}'; restore it from Trash before adding it to focus",
                        entity = opts.entity_label,
                    )));
                }
                Some(None) => {}
            }
        }
    } else {
        let existing: HashSet<String> = stmt
            .query_map(params, |row| row.get::<_, String>(0))?
            .collect::<rusqlite::Result<_>>()?;
        if let Some(missing) = ids.iter().find(|id| !existing.contains(id.as_str())) {
            return Err(McpError::Validation(format!(
                "{field_name} references non-existent {entity} '{missing}'",
                entity = opts.entity_label,
            )));
        }
    }
    Ok(())
}

/// Verify that every task ID references a live, non-archived task.
///
/// `validate_task_ids_exist` only confirms the row is in the
/// `tasks` table — it accepts soft-deleted rows whose `archived_at` is
/// non-NULL. The current-focus surface is a forward-looking plan, so an
/// archived task ID is just as broken as a phantom one: every task read
/// path filters `archived_at IS NULL`, so the focus would render an
/// empty row that the user has no way to recover.
///
/// This helper folds existence + archive checks into a single query
/// and reports the first offending id in caller order. The error
/// message distinguishes the two failure modes so the assistant can
/// react appropriately (re-create vs. restore-from-trash).
pub(crate) fn validate_task_ids_active(
    conn: &Connection,
    task_ids: &[String],
    field_name: &'static str,
) -> Result<(), McpError> {
    validate_ids_referencing(
        conn,
        task_ids,
        field_name,
        IdsValidatorOpts {
            table: "tasks",
            entity_label: "task",
            check_uuid_shape: true,
            reject_archived: true,
        },
    )
}

/// Verify that all list IDs in the array reference existing lists.
///
/// Validates `linked_list_ids` for the daily-review write path before
/// they reach the join-table materializer, so a phantom list ID never
/// lands in `daily_review_lists`. Routes through the shared dedup +
/// single-IN-query engine so a 20-list payload costs one round-trip.
pub(crate) fn validate_list_ids_exist(
    conn: &Connection,
    list_ids: &[String],
    field_name: &'static str,
) -> Result<(), McpError> {
    validate_ids_referencing(
        conn,
        list_ids,
        field_name,
        IdsValidatorOpts {
            table: "lists",
            entity_label: "list",
            check_uuid_shape: false,
            reject_archived: false,
        },
    )
}

